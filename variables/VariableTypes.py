# Variable types in python
# integer
print(type(10))
# float
print(type(10.2))
# string
print(type('Python'))
# boolean
print(type(True))
# dictionary - A dictionary is a collection of key: value pairs.
print(type({'one': 1, 'two': 2, 'three': 3}))
# list - A list is a collection of homogeneous items.
print(type([1, 2, 3]))
# set - A set is an unordered collection with no duplicate elements.
print(type({1, 2, 3}))
# tuple - A tuple is an ordered collection which is unchangeable.
print(type((1, 2, 3)))
